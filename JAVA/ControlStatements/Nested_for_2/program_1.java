//  write a program to print the following pattern
//  C2W1 C2W2 C2W3
//  C2W1 C2W2 C2W3
//  C2W1 C2W2 C2W3


public class program_1 {
    public static void main(String args[]){
        for(int i=0;i<3;i++){
            for(int j=0;j<3;j++){
                System.out.print("C2W1"+" ");
            }
            System.out.println();
        }
    }
}
